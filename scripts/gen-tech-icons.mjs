import * as si from "simple-icons";
import { writeFileSync } from "node:fs";

// marquee item (exact string) -> ordered candidate simple-icons slugs.
// first candidate that exists wins; all-missing => neutral fallback glyph (null).
const MAP = {
  "Claude Code": ["claude", "anthropic"],
  "GPT": ["openai"],
  "Cursor": ["cursor"],
  "FastAPI": ["fastapi"],
  "Django 5": ["django"],
  // DRF is Django-family, but the identical Django "dj" mark next to Django 5
  // reads as a duplication bug — give it a distinct monogram instead.
  "DRF": [],
  "Celery": ["celery"],
  "PostgreSQL": ["postgresql"],
  "Redis": ["redis"],
  "React 19": ["react"],
  "Vue 3": ["vuedotjs"],
  "Vite": ["vite"],
  "TypeScript": ["typescript"],
  "Tailwind CSS": ["tailwindcss"],
  "Pinia": ["pinia"],
  "Radix UI": ["radixui"],
  "Pydantic v2": ["pydantic"],
  // SQLAlchemy's mark is a serif wordmark that turns into a muddy blob at chip
  // scale — route it to the per-tool monogram fallback instead.
  "async SQLAlchemy": [],
  "Docker": ["docker"],
  "Kubernetes": ["kubernetes"],
  "GitHub Actions": ["githubactions"],
  "CodeQL": ["github"],
  "Trivy": ["trivy", "aquasecurity"],
  "cosign": ["sigstore"],
  "Playwright": ["playwright"],
  "Telegram Bot API": ["telegram"],
  "Webhooks": ["webhooks"],
};

const bySlug = (slug) => si["si" + slug.charAt(0).toUpperCase() + slug.slice(1)];

// ---- color helpers -------------------------------------------------------
const hexToRgb = (h) => {
  const n = parseInt(h.replace("#", ""), 16);
  return [(n >> 16) & 255, (n >> 8) & 255, n & 255];
};
const rgbToHex = (r, g, b) =>
  "#" + [r, g, b].map((c) => Math.round(Math.max(0, Math.min(255, c))).toString(16).padStart(2, "0")).join("");
const lin = (c) => {
  const s = c / 255;
  return s <= 0.03928 ? s / 12.92 : ((s + 0.055) / 1.055) ** 2.4;
};
const luminance = ([r, g, b]) => 0.2126 * lin(r) + 0.7152 * lin(g) + 0.0722 * lin(b);
const mix = (rgb, target, t) => rgb.map((c, i) => c * (1 - t) + target[i] * t);

const WHITE = [255, 255, 255];
const NEAR_BLACK = [11, 13, 18];

// color that stays crisp on the DARK navy background (bump dark brands lighter)
function forDark(hex) {
  const rgb = hexToRgb(hex);
  const L = luminance(rgb);
  if (L >= 0.5) return hex;
  const t = Math.min(0.82, (0.62 - L) / (1 - L + 0.001));
  return rgbToHex(...mix(rgb, WHITE, t));
}
// color that stays crisp on the LIGHT background (bump light brands darker).
// Thin outline marks (React atom, Postgres elephant) read faint on white, so
// darken more aggressively than the dark-theme lift.
function forLight(hex) {
  const rgb = hexToRgb(hex);
  const L = luminance(rgb);
  if (L <= 0.42) return hex;
  const t = Math.min(0.86, (L - 0.24) / (L + 0.001));
  return rgbToHex(...mix(rgb, NEAR_BLACK, t));
}

// a few marks whose raw brand color reads poorly on white regardless of
// luminance math (thin cool outlines) — pin a darker, on-brand light-theme tint.
const LIGHT_OVERRIDE = {
  postgresql: "#2f5b82",
};

const out = {};
const report = [];
for (const [item, slugs] of Object.entries(MAP)) {
  let hit = null;
  for (const s of slugs) {
    const icon = bySlug(s);
    if (icon && icon.path) { hit = { slug: s, icon }; break; }
  }
  if (hit) {
    const brand = "#" + hit.icon.hex;
    const light = LIGHT_OVERRIDE[hit.slug] ?? forLight(brand);
    out[item] = { path: hit.icon.path, dark: forDark(brand), light, title: hit.icon.title };
    report.push(`OK   ${item.padEnd(20)} ${hit.slug.padEnd(14)} brand ${brand}  dark ${out[item].dark}  light ${out[item].light}`);
  } else {
    out[item] = null;
    report.push(`MISS ${item.padEnd(20)} (fallback glyph)`);
  }
}

const header = `// AUTO-GENERATED by scripts/gen-tech-icons.mjs from simple-icons — do not edit by hand.
// Each marquee tech label maps to its official brand mark (24x24 path) plus a
// theme-safe fill for the dark navy bg and for the light bg (raw brand colors
// clip on one theme). null => the neutral fallback glyph in <TechIcon>.
export type TechIconData = { path: string; dark: string; light: string; title: string };

export const TECH_ICONS: Record<string, TechIconData | null> = ${JSON.stringify(out, null, 2)};
`;

writeFileSync("src/components/tech-icons-data.ts", header);
